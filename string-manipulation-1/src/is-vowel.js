function isVowel(char) {
  switch (char) {
    case 'a':
      return true;
    case 'A':
      return true;
    case 'e':
      return true;
    case 'E':
      return true;
    case 'i':
      return true;
    case 'I':
      return true;
    case 'o':
      return true;
    case 'O':
      return true;
    case 'u':
      return true;
    case 'U':
      return true;
    default:
      return false;
  }
}
